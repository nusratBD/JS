/* It will sort small to large number */
const numbers = [2, 5, 3, 9, 4, 5, 7, 6];
console.log(numbers.sort());
/* 
output: 
[
  2, 3, 4, 5,
  5, 6, 7, 9
]
*/

/* It will sort according to first letter of name(first to last) */
let names = ['Nusrat', 'Hurain', 'Tithi', 'Dipti', 'Nuha', 'Sweety', 'Koly', 'Coder'];
console.log(names.sort());
/* 
output:
[
  'Coder',  'Dipti',
  'Hurain', 'Koly',
  'Nuha',   'Nusrat',
  'Sweety', 'Tithi'
]
*/