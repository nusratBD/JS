/* How interpreter works? */
/* 
In interpreter, there is an equivalent machine code for every instruction. Like:
console.log(); there is an equivalent machine code for this command. Similarly 
for addition(1+3) there is a specific machine code.

When we instruct interpreter to run codes, it starts running code line by line.
But this process is very slow. But this process is beneficial for debugging easily.
Because as interpret read and execute line by line, it could find out the mistakes
easily and quickly. To reduce the slowness, another process has come called compilation.  
*/